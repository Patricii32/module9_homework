const jsonString =`
{
 "list": [
  {
   "name": "Petr",
   "age": "20",
   "prof": "mechanic"
  },
  {
   "name": "Vova",
   "age": "60",
   "prof": "pilot"
  }
 ]
}
`;
const arr = [];
const data = JSON.parse(jsonString);
const list = data.list;

list.forEach(list =>{
  let person ={
    name: list.name,
    age: list.age,
    prof: list.prof,
   }
  arr.push(person);
});

